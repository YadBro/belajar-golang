package main

import "fmt"

func main() {
	// NUMBERS ===============
	fmt.Println("Ini adalah tipe data number")
	// integer
	fmt.Println(1)
	// floating point number (decimal)
	fmt.Println(1.2)
	// NUMBERS ===============

	// STRING ============
	fmt.Println("Ini adalah tipe data string")
	fmt.Println("Hello World")
	// STRING ============

	// BOOLEAN ==========
	fmt.Println("Ini adalah tipe data boolean")
	fmt.Println(true, false)
	// BOOLEAN ==========
}
